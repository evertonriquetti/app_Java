    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // Converte para float e atribui nas variaveis(peso, altura) os dados digitados nos dois JTextField:
        peso = Float.parseFloat(txtPeso.getText());
        altura = Float.parseFloat(txtAltura.getText());
        
        //Calcula o IMC
        imc = (float) (peso/Math.pow(altura,2));
        
        //Verifica o resultado e diz como est√° seu IMC
        if (imc<=19){
            resultado = "Abaixo do Peso";
        } else if (imc<=25) {
            resultado = "Peso Ideal";
        } else if (imc<=30){
            resultado = "Acima do Peso";
        } else if (imc<=35){
            resultado = "Obesidade leve";
        } else {
            resultado = "Obesidade";
        }
            
        
        //Mostra o resultado do calculo do IMC
        lblResultado.setText(String.valueOf(imc)+" "+resultado);
        
    }//GEN-LAST:event_btnCalcularActionPerformed

